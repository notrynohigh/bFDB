 /******************************************************************************
 * static functions
 ******************************************************************************/
static bFDB_U32 bFDB_StringToU32(bFDB_U8 *pstr)
{
	bFDB_U32 u32_tmp = 0, i = 0, sum = 0;
	bFDB_U32 len = strlen(pstr);
	if(pstr == bFDB_NULL || len == 0)
	{
		return 0;
	}
	for(i = 0;i < len;i++)
	{
		sum += pstr[i] * (i + 1);
	}
	sum = sum / len;
	for(i = 0;i < len;i++)
	{
		u32_tmp += pstr[i] * sum; 
		u32_tmp ^= pstr[i];
	}
	return u32_tmp;
}





